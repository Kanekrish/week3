# Prompt the user to enter the number of apples to remove.
num_apples_to_remove = int(input("How many apples should I remove? "))

# Initialize the variable to track the number of removed apples.
num_apples_removed = 0

# Create a while loop to remove apples.
while num_apples_removed < num_apples_to_remove:

    # Display the message "Removed apple."
    print("Removed apple.")

    # Increment the variable for tracking the number of removed apples.
    num_apples_removed += 1

# Display the message "All apples removed."
print("All apples removed.")
